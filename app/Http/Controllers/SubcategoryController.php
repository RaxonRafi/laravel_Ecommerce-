    {

        $request->validate([
            '*'=> 'required'
        ]);
        Subcategory::insert([
            'category_id'=> $request->category_id,
            'subcategory_name'=> $request->subcategory_name,
            'added_by'=> auth()->id(),
            'created_at'=> Carbon::now(),
        ]);
        return back()->with('success_message','subcategory added successfully');
    }

